def h( position1, position2 ):
	# return 0
	return position2[0] - position1[0] + position2[1] - position1[1]



def reconstructPath( node, lastNode ):
	path = [ node ]

	while node in lastNode:
		node = lastNode[ node ]
		path.insert( 0, node )
		
	return path



def neighbours( position, maze ):
	x = position[0]
	y = position[1]
	width = len( maze )
	height = len( maze[0] )
	
	multiplier = 1000000 - 1
	# multiplier = 3
	
	neighbourList = []
	
	if x > 0:
		neighbourList.append( ( ( x - 1, y ), ( 1 + maze[x - 1][y] * multiplier ) ) )
	
	if x < width - 1:
		neighbourList.append( ( ( x + 1, y ), ( 1 + maze[x + 1][y] * multiplier ) ) )
	
	if y > 0 :
		neighbourList.append( ( ( x, y - 1 ), ( 1 + maze[x][y - 1] * multiplier ) ) )
	
	if y < height - 1:
		neighbourList.append( ( ( x, y + 1 ), ( 1 + maze[x][y + 1] * multiplier ) ) )
	
	return neighbourList



