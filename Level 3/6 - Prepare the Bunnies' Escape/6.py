from collections import deque, defaultdict



def neighbours( maze, node ):
	'''
	Return a list of ( ( x, y ), nodeType ).
	'''
	
	x = node[0]
	y = node[1]
	width = len( maze )
	height = len( maze[0] )
	
	neighboursList = []
	
	if x > 0:
		neighboursList.append( ( ( x - 1, y ), maze[x - 1][y] ) )
	
	if x < width - 1:
		neighboursList.append( ( ( x + 1, y ), maze[x + 1][y] ) )
	
	if y > 0:
		neighboursList.append( ( ( x, y - 1 ), maze[x][y - 1] ) )
	
	if y < height - 1:
		neighboursList.append( ( ( x, y + 1 ), maze[x][y + 1] ) )
	
	return neighboursList



def usssd( graph, source ):
	'''
	Unweighted Single-Source Shortest Distances
	
	Find the distance from `source` to every node in `graph` using breadth-first search.
	'''
	
	discoveredNodes = deque( [ source ] )
	distanceTo = defaultdict( lambda: float( 'inf' ), { source: 0 } )
	
	while discoveredNodes:
		closestNode = discoveredNodes.popleft()
		
		for node, nodeType in neighbours( graph, closestNode ):
			if node not in distanceTo:
				distanceTo[node] = distanceTo[closestNode] + 1
				
				if nodeType == 0:
					discoveredNodes.append( node )
	
	return distanceTo



def solution( maze ):
	'''
	For every wall in the maze, find the shortest distance from start to wall and from end to wall. Then, find the
	shortest total distance among all paths (plus an extra direct path from start to end going through no wall).
	
	Since:
	1) The maximum number of empty cells is `width * height`.
	2) Each cell is connected to at most 4 other cells.
	3) The number of paths is the number of walls, which is also at most `width * height`.
	4) Breadth-first search has a time complexity of O( nodes + edges )
	
	Our solution will have a time complexity of O( width * height ).
	'''
	
	width = len( maze )
	height = len( maze[0] )
	
	start = ( 0, 0 )
	end = ( width - 1, height - 1 )
	
	# Set of all walls plus the end cell.
	goals = { ( x, y ) for y in range( height ) for x in range( width ) if maze[x][y] == 1 }
	goals.add( end )
	
	# Distance to each wall, from the start and from the end.
	distanceFromStart = usssd( maze, start )
	distanceFromEnd = usssd( maze, end )
	
	return min( distanceFromStart[cell] + distanceFromEnd[cell] + 1 for cell in goals )



